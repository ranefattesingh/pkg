package log

import (
	"fmt"
	"math"
	"time"

	"go.uber.org/zap/zapcore"
)

var (
	_minTimeInt64 = time.Unix(0, math.MinInt64)
	_maxTimeInt64 = time.Unix(0, math.MaxInt64)
)

type FieldType uint8

type Field struct {
	Key       string
	Type      FieldType
	Integer   int64
	String    string
	Interface any
}

func (f *Field) toZapField() zapcore.Field {
	return zapcore.Field{
		Key:       f.Key,
		Type:      zapcore.FieldType(f.Type),
		Integer:   f.Integer,
		String:    f.String,
		Interface: f.Interface,
	}
}

func toZapFields(fields ...Field) []zapcore.Field {
	zapfields := []zapcore.Field{}
	for _, field := range fields {
		zapfields = append(zapfields, field.toZapField())
	}

	return zapfields
}

func Err(err error) Field {
	return Field{Key: "error", Type: FieldType(zapcore.ErrorType), Interface: err}
}

// Skip constructs a no-op field, which is often useful when handling invalid
// inputs in other Field constructors.
func Skip() Field {
	return Field{Type: FieldType(zapcore.SkipType)}
}

// nilField returns a field which will marshal explicitly as nil. See motivation
// in https://github.com/uber-go/zap/issues/753 . If we ever make breaking
// changes and add zapcore.NilType and zapcore.ObjectEncoder.AddNil, the
// implementation here should be changed to reflect that.
func nilField(key string) Field { return Reflect(key, nil) }

func Binary(key string, val []byte) Field {
	return Field{Key: key, Type: FieldType(zapcore.BinaryType), Interface: val}
}

// Bool constructs a field that carries a bool.
func Bool(key string, val bool) Field {
	var ival int64
	if val {
		ival = 1
	}
	return Field{Key: key, Type: FieldType(zapcore.BoolType), Integer: ival}
}

// Boolp constructs a field that carries a *bool. The returned Field will safely
// and explicitly represent `nil` when appropriate.
func Boolp(key string, val *bool) Field {
	if val == nil {
		return nilField(key)
	}
	return Bool(key, *val)
}

// ByteString constructs a field that carries UTF-8 encoded text as a []byte.
// To log opaque binary blobs (which aren't necessarily valid UTF-8), use
// Binary.
func ByteString(key string, val []byte) Field {
	return Field{Key: key, Type: FieldType(zapcore.ByteStringType), Interface: val}
}

